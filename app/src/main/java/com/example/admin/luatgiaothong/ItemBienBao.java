public class ItemBienBao {
    public int id;
    public String tenBien;
    public int loaiBien;
    public String noiDung;
    public String anhBien;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenBien() {
        return tenBien;
    }

    public void setTenBien(String tenBien) {
        this.tenBien = tenBien;
    }

    public int getLoaiBien() {
        return loaiBien;
    }

    public void setLoaiBien(int loaiBien) {
        this.loaiBien = loaiBien;
    }

    public String getNoiDung() {
        return noiDung;
    }

    public void setNoiDung(String noiDung) {
        this.noiDung = noiDung;
    }

    public String getAnhBien() {
        return anhBien;
    }

    public void setAnhBien(String anhBien) {
        this.anhBien = anhBien;
    }
}
